public class MatrixTranspose {

    public static void main(String[] args) {

        int[][] matris = {{1,2,3},{4,5,6}};

        for (int i =0;i< matris[1].length;i++){
            for (int j = 0;j<matris.length;j++ ) {


                System.out.print(matris[j][i] + " ");

            }
            System.out.println();





        }



    }
}
